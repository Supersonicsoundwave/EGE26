def convert(num, q):
    res = ''
    while num:
        res += str(num % q)
        num //= q
    return res[::-1]


for x in range(1, 100):
    a = 7*100**6 + 10*100**5 + x*100**4 + 0*100**3 + 1*100**2 + 2*100**1 + 3*100**0
    b = 1*100**5 + 11*100**4 + 10*100**3 + 6*100**2 + 4*100**1 + x*100**0
    c = x*100**6 + 9*100**5 + 8*100**4 + 0*100**3 + 1*100**2 + 2*100**1 + 12*100**0
    f = a - b + c
    if f % 21 == 0:
        print(convert(x, 6))